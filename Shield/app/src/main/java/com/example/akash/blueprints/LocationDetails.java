package com.example.akash.blueprints;

public class LocationDetails {

    private int id;
    private String sCategory, sName, sDescription, sContactNo, sEmailId, sAddress, sLat, sLong;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getsCategory() {
        return sCategory;
    }

    public void setsCategory(String sCategory) {
        this.sCategory = sCategory;
    }

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public String getsDescription() {
        return sDescription;
    }

    public void setsDescription(String sDescription) {
        this.sDescription = sDescription;
    }

    public String getsContactNo() {
        return sContactNo;
    }

    public void setsContactNo(String sContactNo) {
        this.sContactNo = sContactNo;
    }

    public String getsEmailId() {
        return sEmailId;
    }

    public void setsEmailId(String sEmailId) {
        this.sEmailId = sEmailId;
    }

    public String getsAddress() {
        return sAddress;
    }

    public void setsAddress(String sAddress) {
        this.sAddress = sAddress;
    }

    public String getsLat() {
        return sLat;
    }

    public void setsLat(String sLat) {
        this.sLat = sLat;
    }

    public String getsLong() {
        return sLong;
    }

    public void setsLong(String sLong) {
        this.sLong = sLong;
    }

}
